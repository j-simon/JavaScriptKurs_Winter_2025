'use strict';

let vorname = "-Jens!1"

// der Teststring wird vollständig in Kleinbuchstaben gewandelt
// alle anderen Zeichen bleiben erhalten
console.log(vorname.toLowerCase()) // -jens!1

// der Teststring wird vollständig in Grossbuchstaben gewandelt
// alle anderen Zeichen bleiben erhalten
console.log(vorname.toUpperCase()) // -JENS!1